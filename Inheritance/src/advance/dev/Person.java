package advance.dev;

public class Person {

	 String name;
	 int old;
	 String phone;
	
	public Person(String name, int old, String phone) {
		super();
		this.name = name;
		this.old = old;
		this.phone = phone;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getOld() {
		return old;
	}

	public void setOld(int old) {
		this.old = old;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}
	
	
	

}
